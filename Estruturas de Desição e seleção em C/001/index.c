# include <stdio.h>

// Operadores relacionais: ==, !=, >, <, >=, <=

int main() {
    printf("5 == 5: %d\n", 5 == 5); // true (1)
    printf("5 != 5: %d\n", 5 != 5); // false (0)
    printf("5 > 3: %d\n", 5 > 3);   // true (1)
    printf("5 < 3: %d\n", 5 < 3);   // false (0)
    printf("5 >= 5: %d\n", 5 >= 5); // true (1)
    printf("5 <= 3: %d\n", 5 <= 3); // false (0)
    

                    
    return 0;
}
