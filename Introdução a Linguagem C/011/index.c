#include <stdio.h>

int main() {
                    //como adicionar varios valores com o scanf
                    int idade;
                    float altura, peso;

                    printf("\tDigite sua idade, seu peso e sua altura: ");
                    scanf("%d %f %f", &idade, &peso, &altura);
                    printf("\tIdade: %d\n", idade);
                    printf("\tPeso: %.2f\n", peso);                            
                    printf("\tAltura: %.2f\n", altura);











                    


    return 0;
}