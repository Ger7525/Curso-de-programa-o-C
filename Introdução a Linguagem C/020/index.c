# include <stdio.h>

//Casting ou conversão de tipos na linguagem C.
// conversão de tipos é o processo de converter um valor de um tipo de dado para outro tipo de dado.
// Existem dois tipos principais de conversão de tipos em C: conversão implícita e conversão explícita (casting).



int main() {
                   
            float a= 10.5,b = 3.2;      
                   
                   
                   
                   
                   
                   
                    //Exemplo de conversão implícita
                    printf("Resultado : %d\n", (int)(a/b)); // O resultado é 3, pois o valor 10.5 é convertido implicitamente para um inteiro antes da divisão.
                    printf("Resultado normal  : %f\n",a/b);














    return 0;
}