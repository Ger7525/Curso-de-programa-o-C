#include <stdio.h>
int main() {
    //as quatro operações matemáticas em C são: adição (+), subtração (-), multiplicação (*), e divisão (/).

    int a=10, b=5;
    int adicao, subtracao, multiplicacao, divisao;
    
    
    
    
    adicao = a + b;
    subtracao = a - b;
    multiplicacao = a * b;
    divisao = a / b;
    
        
    printf("Adicao: %d\n", adicao);
    printf("Subtracao: %d\n", subtracao);
    printf("Multiplicacao: %d\n", multiplicacao);
    printf("Divisao: %d\n", divisao);
    
    
    
    
                    
    return 0;
}
